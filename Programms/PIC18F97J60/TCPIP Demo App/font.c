static WORD Arial_cp1251[256];
static WORD Arial_B_cp1251[256];

void InitFonts(void)
{
    WORD i = 0;
    WORD j = 0;
    //WORD b = 0;
    WORD_VAL c;
    static BYTE AlreadyInited = 0;
    if(AlreadyInited == 0){
        for(j=0;j<256;j++){
            c.Val = Arial_Data[i];
            Arial_cp1251[c.byte.HB] = i;
            i += c.byte.LB + 1;
        }
        i = 0;
        for(j=0;j<256;j++){
            Arial_B_cp1251[j] = i;
            i += Arial_B_Data[i] + 1;
        }
        AlreadyInited = 1;
    }
}

WORD* GetSymbolImage(BYTE symbol,WORD* OutWordCount,BYTE CFont){
    static WORD Image[12];
    BYTE i;        
    WORD Begin =  0;
    WORD End   =  0;    
    WORD Size =  0;
    WORD_VAL c;
    memset(Image,0,12);
    switch(CFont){
        case 0:
            Begin = Arial_cp1251[symbol];
            c.Val = Arial_Data[Begin];
            Size = c.byte.LB;
        break;
        case 1:
            Begin = Arial_B_cp1251[symbol+1];
            End   = Arial_B_cp1251[(symbol+1)];    
            Size = End - Begin;
        break;
        default:
            End   = 0;
            Begin = 0;
    }   
    //Image[0]=0;
    if(Size<1){
        (*OutWordCount) = 0;
        return 0;
    } 
     switch(CFont){
        case 0:
            //memcpy(Image,&Arial_Data[Begin],Size*sizeof(WORD));
            for(i=0;i<Size;i++){
                Image[i]= Arial_Data[Begin+i+1];
            }
        break;
        case 1:
            for(i=0;i<Size;i++){
                Image[i]= Arial_B_Data[Begin+i];
            }
        break;
     }   
    (*OutWordCount) = Size;
    return Image;//&ArialData[Begin];    
}

